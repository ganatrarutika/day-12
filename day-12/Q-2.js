// Step 1: Define the array called numbers
const numbers = [1, 2, 3];

// Step 2: Initialize a variable to hold the sum
let sum = 0;

// Step 3: Use a loop to iterate through the array and calculate the sum
for (let i = 0; i < numbers.length; i++) {
    sum += numbers[i];
}

// Print the sum to the console
console.log('Sum of all numbers:', sum);
